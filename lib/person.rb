class Person 
    # Setter method for person's name
    def name=(name)
        @name = name
    end

    #Getter method for person's name
    def name
        @name
    end

    # Setter method for person's job
    def job=(job)
        @job = job
    end

    #Getter method for person's job
    def job
        @job
    end
end